pub fn script(c: char) -> Script {
    let c = c as u32;
    match SCRIPT.binary_search_by(|&(lo, hi, _)| {
        if hi < c { Less }
        else if c < lo { Greater }
        else { Equal }
    }) {
        Ok(idx) => SCRIPT[idx].2,
        Err(_) => Script::Unknown
    }
}

